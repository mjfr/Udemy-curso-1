# Operadores de comparação são comparadores que resultam em booleanos (Veradeiro ou Falso)
# Operadores:


# == Operador de igualdade, se uma condição apresentar igualdade, retorna verdadeiro
1 == 1 #Verdadeiro
1 == 3 #Falso

# != Operador de diferença, se uma condição apresentar diferença, retorna verdadeiro
'Oi' != 'Oi' #Falso
'Oi' != 'Olá' #Verdadeiro

# <> Operador de diferença, semelhante ao !=  -------> NÃO É MAIS SUPORTADO NO PYTHON 3
#######################################################################################

# > Operador maior que, se uma condição for maior que a outra, retorna verdadeiro
1 > 2 #Falso
7 > 5 #Verdadeiro

# < Operador menor que, se uma condição for menor que a outra, retorna falso
4 < 4 #Falso
1 < 9 #Verdadeiro

# >= Operador maior ou igual a, se uma condição for igual ou maior que a outra, retorna verdadeiro
4 >= 4 #Verdadeiro
7 >= 14 #Falso

# <= Operador menor ou igual a, se uma condição for igual ou menor que a outra, retorna verdadeiro
8 <= 8 #Verdadeiro
5 <= 0 #Falso
