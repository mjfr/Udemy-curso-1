# Tuplas são semelhantes a listas, a diferença inicial é o início com parênteses
tupla = (1, 2, 3)
tupla

# Também iguais as listas, as tuplas aceitam múltiplos tipos de dados
t = ('string', 1, 2.3, [1, 2, 'lista'], {'chave1':'valor1', 'chave2':2}, 1, 1, 34)
t

# Métodos imbutidos nas tuplas
# O método .count() conta quantas vezes um certo elemento aparece nas tuplas
t.count(1)

# Tuplas são imutáveis, logo, diferente das listas, as tuplas não aceitam associações nem novas inserções de dados.
