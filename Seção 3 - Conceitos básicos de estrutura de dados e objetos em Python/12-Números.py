1 + 1
    # Subtração
1 - 1
    # Divisão
6 / 3
    # Multiplicação
2 * 3
    # Exponenciação
2 ** 6
    # Radiciação (Raiz quadrada)
9 ** 0.5
    # Radiciação (Raiz cúbica, método ruim)
27 ** 0.3333333333333333
