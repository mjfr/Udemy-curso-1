# A criação de uma lista é a criação de um objeto. Ao atribuir a criação de uma lista a uma variável, estamos
#  dizendo que a variável é um objeto do tipo lista onde lista é uma classe
# Em Python tudo é objeto.

# Criando uma lista e atribuíndo ela a uma variável
l = [1, 2, 3]

# O tipo da variável l é 'list' que é uma classe
type(l)

# Criando uma função
def funcao(a, b):
    return a + b

# O tipo da função é uma 'function' que também é uma classe
type(funcao)